inline void throwErrorAtLocation(const Clingo::Location &location, const char *errorMessage)
{
	std::cerr
		<< location.begin_file() << ":"
		<< location.begin_line() << ":" << location.begin_column()
		<< ": error: "
		<< errorMessage << std::endl;
}

